[Flags]
public enum Anchor {
    Bottom = 1<<0,
    Top = 1<<1,
    Left = 1<<2,
    Right = 1<<3,
    Center = 1<<4,
    
    CenterBottom = Bottom | Center,
    CenterTop = Top | Center,
    LeftBottom = Bottom | Left,
    LeftTop = Top | Left,
    RightBottom = Bottom | Right,
    RightTop = Top | Right,
}

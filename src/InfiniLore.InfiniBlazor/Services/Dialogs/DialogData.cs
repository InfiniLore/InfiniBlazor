public record DialogData(
    Type ComponentType,
    Guid Id
) : IDialogData {
    // -----------------------------------------------------------------------------------------------------------------
    // Constructors
    // -----------------------------------------------------------------------------------------------------------------
    public static DialogData Create<T>(Dictionary<string, object?>? parameters = null) where T : InfiniDialogBase => new(
        typeof(T),
        Guid.CreateVersion7()
    );
    
    // -----------------------------------------------------------------------------------------------------------------
    // Methods
    // -----------------------------------------------------------------------------------------------------------------
    public virtual IDictionary<string, object?>? AsDynamicParameters() => null;
}

using System.Collections.Frozen;

namespace InfiniLore.InfiniBlazor.MarkdownParser.EmoteSystem;
// ---------------------------------------------------------------------------------------------------------------------
// Code
// ---------------------------------------------------------------------------------------------------------------------
public sealed record EmoteKey(params FrozenSet<string> Keys) {
    // -----------------------------------------------------------------------------------------------------------------
    // Constructors
    // -----------------------------------------------------------------------------------------------------------------
    public static EmoteKey FromPossibleKeys(params FrozenSet<string> keys) => new(keys);

    // -----------------------------------------------------------------------------------------------------------------
    // Methods
    // -----------------------------------------------------------------------------------------------------------------
    public bool ContainsKey(string key) => Keys.Contains(key);
    public bool ContainsKey(ReadOnlySpan<char> key) => Keys.GetAlternateLookup<ReadOnlySpan<char>>().Contains(key);
}
